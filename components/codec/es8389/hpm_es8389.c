hpm_stat_t es8389_config_clock(codec_control_t *control, uint32_t mclk, uint32_t sample_rate)
{
    hpm_stat_t stat = status_success;

    uint32_t ratio = (uint32_t)((float)(mclk) / (sample_rate) + 0.5f);

    int8_t coeff = get_coeff(mclk, ratio, sizeof(coeff_div) / sizeof(struct _coeff_div), coeff_div);
    if (coeff < 0) {
        return status_invalid_argument;
    }

    /* Calculate SCLK_DIV in master mode */
    uint8_t reg_val;
    es8389_read_reg(control, ES8389_REG0x01_MISC_CONTROL, &reg_val);
    if ((reg_val & ES8389_MISC_CONTROL_MS_MODE_MASK)) {
        uint16_t sclk_div = mclk / (sample_rate * 2 * 32); /* Assume 2 channels, 32 bits per channel */
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x0B_MASTER_SCLK_DIVIDER, sclk_div));
    }

    /* configure LRCK divider */
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x0C_MASTER_LRCK_DIVIDER_1,
                                    ES8389_MASTER_LRCK_DIVIDER_1_MST_LRCK_DIVH_MASK, ES8389_MASTER_LRCK_DIVIDER_1_MST_LRCK_DIVH_SET(ratio >> 8)));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x0D_MASTER_LRCK_DIVIDER_2, ES8389_MASTER_LRCK_DIVIDER_2_MST_LRCK_DIVL_SET(ratio)));

    if (coeff >= 0) {
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x04_MAIN_CLOCK_DIVIDER1, coeff_div[coeff].reg0x04));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x05_MASTER_CLOCK_MULTIPLICATION, coeff_div[coeff].reg0x05));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x06_MAIN_CLOCK_MUX1, coeff_div[coeff].reg0x06));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x07_MAIN_CLOCK_MUX_2, coeff_div[coeff].reg0x07));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x08_ADC_CLOCK_CONTROL, coeff_div[coeff].reg0x08));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x09_DAC_CLOCK_CONTROL_1, coeff_div[coeff].reg0x09));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x0A_DAC_CLOCK_CONTROL_2, coeff_div[coeff].reg0x0a));
        HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x0F_OSC_CLOCK_CONTROL, 0xC0, coeff_div[coeff].reg0x0f));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x11_MAIN_CLOCK_DIVIDER_2, coeff_div[coeff].reg0x11));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x21_ADC_OSR_CONTROL, coeff_div[coeff].reg0x21));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x22_ADC_DSP_COUNTER_CONTROL, coeff_div[coeff].reg0x22));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x26_ADC_OSR_VOLUME_CONTROL, coeff_div[coeff].reg0x26));
        HPM_CHECK_RET(es8389_modify_reg(control, EVEREST_REG0x30, 0xC0, coeff_div[coeff].reg0x30));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x41_DAC_DSM_COUNTER_CONTROL, coeff_div[coeff].reg0x41));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x42_DAC_DSP_COUNTER_CONTROL, coeff_div[coeff].reg0x42));
        HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x43_DAC_MISC_CONTROL_1, 0x81, coeff_div[coeff].reg0x43));
        HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0xF0_CHIP_MISC_CONTROL, 0x73, coeff_div[coeff].reg0xf0));
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0xF1_CSM_STATE_REPORT, coeff_div[coeff].reg0xf1));
        HPM_CHECK_RET(es8389_write_reg(control, EVEREST_REG0x16, coeff_div[coeff].reg0x16));
        HPM_CHECK_RET(es8389_write_reg(control, EVEREST_REG0x18, coeff_div[coeff].reg0x18));
        HPM_CHECK_RET(es8389_write_reg(control, EVEREST_REG0x19, coeff_div[coeff].reg0x19));
    }

    return stat;
}

hpm_stat_t es8389_set_protocol(codec_control_t *control, es8389_protocol_t protocol, es8389_lrclk_polarity_t lrclk_polarity)
{
    hpm_stat_t stat = status_success;
    bool invert_lrck = false;
    switch (protocol) {
    case es8389_protocol_i2s:
        if (lrclk_polarity == es8389_lrclk_polarity_high_for_left_channel) {
            invert_lrck = true;
        } else {
            invert_lrck = false;
        }
        break;
    case es8389_protocol_left_justified:
        if (lrclk_polarity == es8389_lrclk_polarity_low_for_left_channel) {
            invert_lrck = true;
        } else {
            invert_lrck = false;
        }
        break;
    case es8389_protocol_pcma:
        invert_lrck = false;
        break;
    case es8389_protocol_pcmb:
        invert_lrck = true;
        break;
    default:
        invert_lrck = false;
        break;
    }

    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x0C_MASTER_LRCK_DIVIDER_1, ES8389_MASTER_LRCK_DIVIDER_1_MST_SP_LRP_MASK,
                                    ES8389_MASTER_LRCK_DIVIDER_1_MST_SP_LRP_SET(invert_lrck)));
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x0C_MASTER_LRCK_DIVIDER_1, ES8389_MASTER_LRCK_DIVIDER_1_MST_SP_PTL_MASK,
                                    ES8389_MASTER_LRCK_DIVIDER_1_MST_SP_PTL_SET(protocol)));

    HPM_CHECK_RET(
        es8389_modify_reg(control, ES8389_REG0x20_ADC_SP_CONTROL, ES8389_ADC_SP_CONTROL_ADC_SP_LRP_MASK, ES8389_ADC_SP_CONTROL_ADC_SP_LRP_SET(invert_lrck)));
    HPM_CHECK_RET(
        es8389_modify_reg(control, ES8389_REG0x20_ADC_SP_CONTROL, ES8389_ADC_SP_CONTROL_ADC_SP_PTL_MASK, ES8389_ADC_SP_CONTROL_ADC_SP_PTL_SET(protocol)));

    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x40_DAC_SERIAL_PORT_CONTROL, ES8389_DAC_SERIAL_PORT_CONTROL_DAC_SP_LRP_MASK,
                                    ES8389_DAC_SERIAL_PORT_CONTROL_DAC_SP_LRP_SET(invert_lrck)));
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x40_DAC_SERIAL_PORT_CONTROL, ES8389_DAC_SERIAL_PORT_CONTROL_DAC_SP_PTL_MASK,
                                    ES8389_DAC_SERIAL_PORT_CONTROL_DAC_SP_PTL_SET(protocol)));

    return stat;
}

hpm_stat_t es8389_set_data_width(codec_control_t *control, uint8_t data_width)
{
    hpm_stat_t stat = status_success;

    uint8_t val = 0;
    switch (data_width) {
    case 24:
        val = 0x0;
        break;
    case 20:
        val = 0x1;
        break;
    case 18:
        val = 0x2;
        break;
    case 16:
        val = 0x3;
        break;
    case 32:
        val = 0x4;
        break;
    default:
        return status_invalid_argument;
    }
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x20_ADC_SP_CONTROL, ES8389_ADC_SP_CONTROL_ADC_SP_WL_MASK, ES8389_ADC_SP_CONTROL_ADC_SP_WL_SET(val)));
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x40_DAC_SERIAL_PORT_CONTROL, ES8389_DAC_SERIAL_PORT_CONTROL_DAC_SP_WL_MASK,
                                    ES8389_DAC_SERIAL_PORT_CONTROL_DAC_SP_WL_SET(val)));
    return stat;
}

hpm_stat_t es8389_set_data_format(codec_control_t *control, uint32_t mclk, uint32_t sample_rate, uint8_t data_width)
{
    hpm_stat_t stat = status_success;

    HPM_CHECK_RET(es8389_config_clock(control, mclk, sample_rate));
    HPM_CHECK_RET(es8389_set_data_width(control, data_width));

    return stat;
}

/* Everest chip enters bias_on (power on) state */
hpm_stat_t es8389_enter_power_on(codec_control_t *control)
{
    hpm_stat_t stat = status_success;
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x4D_DAC_RESET_CONTROL, 0x02));
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x69_DAC_ANALOG_CONTROL, 0x20, 0x20));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x61_ANALOG_ENABLE_CONTROL_1, 0xD9));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x64_ADC_ANALOG_ENABLE_CONTROL, 0x8F));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x10_CSM_JUMP_CONTROL, 0xE4));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x00_RESET_CONTROL, 0x01));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x03_CLOCK_OFF1, 0xC3));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x24_ADC_HPF_CONTROL_1, 0x6A));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x25_ADC_HPF_CONTROL_2, 0x0A + (ES8389_ADC1_Invert << 6) + (ES8389_ADC2_Invert << 5)));
    return stat;
}

/* Everest chip enters Standby state */
hpm_stat_t es8389_enter_standby(codec_control_t *control)
{
    hpm_stat_t stat = status_success;
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x40_DAC_SERIAL_PORT_CONTROL, 0x03, 0x03)); /* DAC MUTE, prevent large signal during STANDBY causing additional POP */
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x10_CSM_JUMP_CONTROL, 0xD4));
    board_delay_ms(70);
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x61_ANALOG_ENABLE_CONTROL_1, 0x59));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x64_ADC_ANALOG_ENABLE_CONTROL, 0x00));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x03_CLOCK_OFF1, 0x00));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x00_RESET_CONTROL, 0x7E));
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x40_DAC_SERIAL_PORT_CONTROL, 0x03, 0x00)); /* Release MUTE after closing */
    return stat;
}

/* Everest chip enters bias_off (power down) state */
hpm_stat_t es8389_enter_power_down(codec_control_t *control)
{
    hpm_stat_t stat = status_success;
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x01_MISC_CONTROL, 0x28));
    HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x69_DAC_ANALOG_CONTROL, 0x20, 0x00));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x60_VMID_CONTROL, 0x00));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x00_RESET_CONTROL, 0x00));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x10_CSM_JUMP_CONTROL, 0xCC));
    board_delay_ms(500);
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x10_CSM_JUMP_CONTROL, 0x00));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x61_ANALOG_ENABLE_CONTROL_1, 0x08));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0xF3_ISOLATION_CONTROL, 0xC1));
    HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0xF2_PULL_DOWN_CONTROL, 0x00));
    return stat;
}

hpm_stat_t es8389_set_volume(codec_control_t *control, es8389_module_t module, uint8_t volume)
{
    hpm_stat_t stat = status_success;

    switch (module) {
    case es8389_adc1:
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x27_ADC1_VOLUME_CONTROL, volume));
        break;
    case es8389_adc2:
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x28_ADC2_VOLUME_CONTROL, volume));
        break;
    case es8389_dac1:
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x46_DAC1_VOLUME_CONTROL, volume));
        break;
    case es8389_dac2:
        HPM_CHECK_RET(es8389_write_reg(control, ES8389_REG0x47_DAC2_VOLUME_CONTROL, volume));
        break;
    default:
        return status_invalid_argument;
    }

    return stat;
}

hpm_stat_t es8389_mute(codec_control_t *control, es8389_module_t module, bool mute)
{
    hpm_stat_t stat = status_success;

    switch (module) {
    case es8389_adc1:
        HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x20_ADC_SP_CONTROL, ES8389_ADC_SP_CONTROL_ADC1_P2S_SWMUTE_MASK, ES8389_ADC_SP_CONTROL_ADC1_P2S_SWMUTE_SET(mute)));
        break;
    case es8389_adc2:
        HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x20_ADC_SP_CONTROL, ES8389_ADC_SP_CONTROL_ADC2_P2S_SWMUTE_MASK, ES8389_ADC_SP_CONTROL_ADC2_P2S_SWMUTE_SET(mute)));
        break;
    case es8389_dac1:
        HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x40_DAC_SERIAL_PORT_CONTROL, ES8389_DAC_SERIAL_PORT_CONTROL_DAC1_S2P_SWMUTE_MASK, ES8389_DAC_SERIAL_PORT_CONTROL_DAC1_S2P_SWMUTE_SET(mute)));
        break;
    case es8389_dac2:
        HPM_CHECK_RET(es8389_modify_reg(control, ES8389_REG0x40_DAC_SERIAL_PORT_CONTROL, ES8389_DAC_SERIAL_PORT_CONTROL_DAC2_S2P_SWMUTE_MASK, ES8389_DAC_SERIAL_PORT_CONTROL_DAC2_S2P_SWMUTE_SET(mute)));
        break;
    default:
        return status_invalid_argument;
    }

    return stat;
}


