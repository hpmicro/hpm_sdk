static inline int8_t get_coeff(uint32_t mclk, uint32_t rate, uint8_t array_size, const struct _coeff_div *coeff_div)
{
    int8_t i;
    for (i = 0; i < array_size; i++) {
        if ((coeff_div[i].ratio == rate) && (coeff_div[i].mclk == mclk)) {
            return i;
        }
    }
    return -1;
}

static hpm_stat_t es8389_write_reg(codec_control_t *control, uint8_t reg, uint8_t val)
{
    return i2c_master_address_write(control->ptr, control->slave_address, &reg, 1U, &val, 1U);
}

static hpm_stat_t es8389_read_reg(codec_control_t *control, uint8_t reg, uint8_t *val)
{
    return i2c_master_address_read(control->ptr, control->slave_address, &reg, 1U, val, 1U);
}

static hpm_stat_t es8389_modify_reg(codec_control_t *control, uint8_t reg, uint8_t clear_mask, uint8_t val)
{
    hpm_stat_t status;
    uint8_t reg_val;

    status = es8389_read_reg(control, reg, &reg_val);
    if (status != status_success) {
        return status;
    }

    reg_val = (reg_val & ~clear_mask) | (val & clear_mask);
    return es8389_write_reg(control, reg, reg_val);
}

void es8389_get_default_config(es8389_config_t *config)
{
    assert(config != NULL);

    /* Initializes the configure structure to zero. */
    memset(config, 0, sizeof(*config));

    config->master_mode = false;
    config->mclk_hz = 0U;
    config->sample_rate = 48000U;
    config->data_width = 32U;
    config->protocol = es8389_protocol_left_justified;
    config->lrclk_polarity = es8389_lrclk_polarity_low_for_left_channel;
}

